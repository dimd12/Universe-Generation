package com.universe.enums.types;

/**
 * Represents the main categories of galaxies,
 * based on their overall structure and stellar distribution.
 *
 * <p>Example: The Milky Way is a {@link #SPIRAL} galaxy.</p>
 *
 * @author Adrian
 * @since 1.0
 * @version 1.0
 */
public enum GalaxyTypes {

    /** Spiral galaxy with arms winding out from a central bulge (e.g., Milky Way, Andromeda). */
    SPIRAL,

    /** Elliptical galaxy with an ellipsoidal shape and little internal structure. */
    ELLIPTICAL,

    /** Irregular galaxy lacking a defined shape, often chaotic in appearance. */
    IRREGULAR
}
